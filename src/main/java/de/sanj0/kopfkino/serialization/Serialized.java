package de.sanj0.kopfkino.serialization;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Target(ElementType.FIELD)
@Retention(RetentionPolicy.RUNTIME)
public @interface Serialized {

    /**
     * The key of the value that this field
     * is stored in and read from, relative to its
     * parent's {@link Serializable#id() sanjo class}.
     *
     * @return the key of the value associated with this field
     * @see de.edgelord.sanjo.SJValue
     */
    String key() default "";
}
